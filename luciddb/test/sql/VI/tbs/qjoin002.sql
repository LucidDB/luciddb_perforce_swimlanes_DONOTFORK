-- Simple SELF JOIN tests
-- Cover the following join types
-- b) NOT IN
-- c) NOT BETWEEN


-- NOT IN JOIN

SELECT B1.KSEQ, B1.K2, B2.K2, B2.K4 FROM BENCH100 B1, BENCH100 B2 
WHERE B1.K2 NOT IN (B2.K2, B2.K4)  AND B1.KSEQ < 20
ORDER BY B1.KSEQ, B1.K2, B2.K2, B2.K4
;

SELECT B1.KSEQ, B1.K2, B2.K2, B2.K5 FROM BENCH100 B1, BENCH100 B2 
