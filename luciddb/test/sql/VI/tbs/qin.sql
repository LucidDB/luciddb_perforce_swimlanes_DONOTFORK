set schema 's';

-- indexed
SELECT KSEQ FROM BENCH100 WHERE K4 IN (1,2) ORDER BY 1;

-- non indexed
SELECT KSEQ FROM BENCH100 WHERE K25 IN (1,2,5,7) ORDER BY 1;

-- both
SELECT KSEQ FROM BENCH100 WHERE K4 IN (3,4) or K10 IN (9,4,7) ORDER BY 1;

-- out of range values
SELECT KSEQ FROM BENCH100 WHERE K4 IN (1,2,100,-25) ORDER BY 1;

SELECT KSEQ FROM BENCH100 WHERE K25 IN (1,2,100,-25) ORDER BY 1;

-- duplicate values
SELECT KSEQ FROM BENCH100 WHERE K4 IN (1,2,1,2,2,3) ORDER BY 1;
SELECT KSEQ FROM BENCH100 WHERE K25 IN (1,2,3,3,25,1) ORDER BY 1;              
