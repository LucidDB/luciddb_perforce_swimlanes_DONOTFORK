SELECT B1.KSEQ, B2.KSEQ
FROM BENCH1M B2, BENCH1M B1
WHERE
     B1.K40K = 99
AND  B1.K250K = B2.K500K
AND  B2.K25 = 19
ORDER BY 1,2
;

