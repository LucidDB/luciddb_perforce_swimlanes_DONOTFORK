package org.eigenbase.oj.stmt;

import java.sql.*;


/**
 * PreparedResult represents the result of a call to Statement.prepareSql.
 *
 * @author John V. Sichi
 * @version $Id$
 */
public interface PreparedResult
{
    //~ Methods ---------------------------------------------------------------

    /**
     * @return the code generated by preparation
     */
    String getCode();

    /**
     * @return true iff this result is for a DML statement, in which case the
     *         result set is one row with one column containing the number of
     *         rows affected.
     */
    boolean isDml();

    /**
     * Execute the prepared result.
     *
     * @return producer of rows resulting from execution
     */
    Object execute();
}


// End PreparedResult.java
